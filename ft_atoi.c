int		ft_atoi(const char *str)
{
	int a;
	int i;
	int signe;

	a = 0;
	i = 0;
	signe = 1;
	while (str[a] == '\n' || str[a] == '\t' || str[a] == '\v' || str[a] == '\f'
	|| str[a] == '\r' || str[a] == ' ')
		a++;
	if (str[a] == '-' || str[a] == '+')
	{
		if (str[a] == '-')
			signe = -signe;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		i = i * 10 + str[a] - 48;
		a++;
	}
	return (i * signe);
}
