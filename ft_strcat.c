#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	char *a;

	a = dst + (ft_strlen(dst));
	while (*src != '\0')
	{
		*a = *src;
		a++;
		src++;
	}
	*a = '\0';
	return (dst);
}
