#include "libft.h"

char	*ft_strncat(char *dst, const char *src, size_t n)
{
	char *a;

	a = dst + (ft_strlen(dst));
	while (*src != '\0' && n-- > 0)
	{
		*a = *src;
		a++;
		src++;
	}
	*a = '\0';
	return (dst);
}
